    private void adminDropClassButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminDropClassButtonActionPerformed
        String courseCode = (String) adminDropClassComboBox.getSelectedItem();
        String semester = currentSemester;
        
        if (courseCode == null || courseCode.isEmpty()) {
            adminDropClassTextArea.setText("No classes available to drop.");
            return;
        }

        // Get all schedule entries (scheduled + waitlisted)
        ArrayList<ScheduleEntry> schedule = ScheduleQueries.getScheduleByCourse(semester, courseCode);

        StringBuilder result = new StringBuilder();
        result.append("Scheduled students dropped from the course:\n");

        for (ScheduleEntry entry : schedule) {
            if (entry.getStatus().equals("S")) {
                result.append(entry.getStudentID()).append(" - Scheduled\n");
            }
        }

        result.append("\nWaitlisted students dropped from the course:\n");

        for (ScheduleEntry entry : schedule) {
            if (entry.getStatus().equals("W")) {
                result.append(entry.getStudentID()).append(" - Waitlisted\n");
            }
        }

        // Drop from Schedule and Class
        ScheduleQueries.dropScheduleByCourse(semester, courseCode);
        ClassQueries.dropClass(semester, courseCode);

        // Refresh components
        rebuildClassComboBoxes();
        rebuildScheduleClassComboBoxes();

        // Show result
        adminDropClassTextArea.setText(result.toString());

    }//GEN-LAST:event_adminDropClassButtonActionPerformed

    private void checkData() {
        try {
            FileReader reader = new FileReader("xzq789yy.txt");
            BufferedReader breader = new BufferedReader(reader);

            String encodedAuthor = breader.readLine();
            String encodedProject = breader.readLine();
            byte[] decodedAuthor = Base64.getDecoder().decode(encodedAuthor);
            author = new String(decodedAuthor);
            byte[] decodedProject = Base64.getDecoder().decode(encodedProject);
            projectDate = new String(decodedProject);
            reader.close();

        } catch (FileNotFoundException e) {
            //get user info and create file
            author = JOptionPane.showInputDialog("Enter your first and last name.");
            projectDate = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime()).toString();

            //write data to the data file.
            try {
                FileWriter writer = new FileWriter("xzq789yy.txt", true);
                BufferedWriter bufferedWriter = new BufferedWriter(writer);

                // encode the output data.
                String encodedAuthor = Base64.getEncoder().encodeToString(author.getBytes());

                bufferedWriter.write(encodedAuthor);
                bufferedWriter.newLine();

                String encodedProject = Base64.getEncoder().encodeToString(projectDate.getBytes());
                bufferedWriter.write(encodedProject);

                bufferedWriter.close();
            } catch (IOException ioe) {
                ioe.printStackTrace();
                System.exit(1);
            }

        } catch (IOException e) {
            e.printStackTrace();
            System.exit(1);
        }

    }

