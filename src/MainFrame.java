    public void rebuildSemesterComboBoxes() {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel<>(semesters.toArray(new String[0])));
        if (semesters.size() > 0) {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        } else {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }
    
    private void rebuildClassComboBoxes() {
        addClassCourseCodeComboBox.removeAllItems();
        ArrayList<String> courseCodes = CourseQueries.getAllCourseCodes();
        for (String code : courseCodes) {
            addClassCourseCodeComboBox.addItem(code);
        }

        addClassSeatsComboBox.removeAllItems();
        addClassSeatsComboBox.addItem("2");
        
        displayClassListComboBox.removeAllItems();
        adminDropClassComboBox.removeAllItems();
        ArrayList<ClassEntry> classes = ClassQueries.getAllClasses(currentSemester);
        for (ClassEntry entry : classes) {
            displayClassListComboBox.addItem(entry.getCourseCode());
            adminDropClassComboBox.addItem(entry.getCourseCode());
        }
    }
     
    
    
    private void rebuildScheduleClassComboBoxes() {
        // Fill the course code combo box
        scheduleClassSelectClassComboBox.removeAllItems();
        ArrayList<String> courseCodes = CourseQueries.getAllCourseCodes();
        for (String code : courseCodes) {
            scheduleClassSelectClassComboBox.addItem(code);
        }

        // Fill the student ID combo box
        scheduleClassSelectStudentComboBox.removeAllItems();
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        for (StudentEntry student : students) {
            scheduleClassSelectStudentComboBox.addItem(student.getStudentID());
        }
    }
    
    private void rebuildDisplayStudentScheduleComboBox() {
        displayStudentScheduleComboBox.removeAllItems();
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        for (StudentEntry student : students) {
            displayStudentScheduleComboBox.addItem(student.getStudentID());
        }
    }
    


    

